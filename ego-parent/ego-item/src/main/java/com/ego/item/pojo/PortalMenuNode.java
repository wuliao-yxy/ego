package com.ego.item.pojo;

import java.util.List;

/**
 * 每个节点的类型
 */
public class PortalMenuNode {
    public String u;
    public String n;
    public List<Object> i;

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public List<Object> getI() {
        return i;
    }

    public void setI(List<Object> i) {
        this.i = i;
    }
}
